#define MAX_LED 8 //goober number should switch to the value in series
#define USE_BRIGHTNESS 1 //binary setting for on or off

//4x4 matrix to represent the used LEDs may need to expand this number if the LED_MAX is increased
uint8_t LED_Data[MAX_LED][4]; //for color (i think)
uint8_t LED_Mod[MAX_LED][4]; //for individual brightness

int datasentflag = 0; //initialization for the DMA start

void HAL_TIM_PWM_PulseFinishedCallback(TIM_HandleTypeDef *htim)
{
	HAL_TIM_PWM_Stop_DMA(&htim1, TIM_CHANNEL_1);
	datasentflag = 1; //DMA set for after DMA is completed
}

void Set_LED (int LEDnum, int Red, int Green, int Blue)
{
	LED_Data[LEDnum][0] = LEDnum;
	LED_Data[LEDnum][1] = Green;
	LED_Data[LEDnum][2] = Red;
	LED_Data[LEDnum][3] = Blue;
}

#define PI 3.14159265

void Set_Brightness (int brightness) //sets brightness for the LED(s), self explanatory
//remember that the scale goes from 0-45 | examples- 255 = 100%; 127 = 50%; 63 = 25%; (brightness ratios for the full brightness amount
{
#if USE_BRIGHTNESS

	if (brightness > 45) brightness = 45;
	for (int i = 0; i < MAX_LED; i++)
	{
		LED_Mod[i][0] = LED_Data[i][0];
		for (int j = 1; j < 4; j++)
		{
			float angle = 90 - brightness; //in degrees
			angle = angle * PI / 180; //in radians
			LED_Mod[i][j] = (LED_Data[i][j])/(tan(angle)); //this shit it done to change the linearity, not my solution
		}
	}
#endif
}

uint16_t pwmData[(24*MAX_LED) + 50]; //the 24 bits is per LED and any more is shifted to the next one
//you can store 24 times the LEDs plus a flat 50 values
//the extra 50 is the 50 uSecond reset that is used in-between transmissions

//recall that the pwm width is 1.25 uSeconds
void WS2812_Send (void) //function used to send the values to the pwm dma
{
	uint32_t indx = 0;
	uint32_t color; //32 bit variable used for the 24 bit color data (the cringe color data stream)

	for (int i = 0; i < MAX_LED; i++) //performs the operation for every LED individually
	{
		color = ((LED_Mod[i][1]<<16) /*Green*/ | (LED_Mod[i][2]<<8) /*Red*/ | LED_Mod[i][3]); /*Blue*/
		//green code (value) in the second column, red in the third column, blue in the fourth column
		//a 16 bit shift is done for green, 8 bit for red, and no shift for blue
		for (int i = 23; i >= 0; i--)
		{
			if (color&(1<<i)) //now individual bits in the color variable are checked here, for linearity i'm guessing
			{
				//if MSB (most sig bit) is 1, then 60 is stored in LSB (least sig bit)
				pwmData[indx] = 60; // 2/3rds of the duty cycle
			}

			else pwmData[indx] = 30; // 1/3rd of the duty cycle

			indx++;
		}
	}

	for (int i = 0; i < 50; i++) //need to add 50 zeros lol, check the data sheet. Unironically the 60 uSecond reset code
	{
		pwmData[indx] = 0;
		indx++;
	}
	//once LED values are stored then it is iterated through the matrix

	HAL_TIM_PWM_Start_DMA(&htim1, TIM_CHANNEL_1, (uint32_t *)pwmData, indx); //after all the storing is finished we send the data to the dma
	while(!datasentflag){}; //after sending all the data, we wait for the flag to set; indicates that DMA is stopped and another set can now be sent
	datasentflag = 0;
}

